#Initialises variable
MyString = "apple pie"

#Skips one letter of the variable 
print(MyString[0:len(MyString):2])

#Prints the variable backwards
print(MyString[::-1])

#Prints until the 2nd character of the string
print(MyString[:2])

#Skips 2 letters of the variable
print(MyString[0:len(MyString):3])
